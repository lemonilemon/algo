// constants
const int MAXN = 2e5 + 5;
vector<int> graph[MAXN];
int tl[MAXN], tr[MAXN], timer, fa[MAXN], dep[MAXN];
void dfs1(int u, int p = -1) {
    tl[u] = ++timer;
    if(p == -1) dep[u] = 0;
    else dep[u] = dep[p] + 1;
    fa[u] = p;
    for(int v : graph[u]) {
        if(v == p) continue;
        dfs1(v, u);
    }
    tr[u] = timer;
}

// solution
void solve() {
    int n, k; 
    cin >> n >> k;
    for(int i = 0; i < n - 1; ++i) {
        int u, v;
        cin >> u >> v;
        graph[u].push_back(v);
        graph[v].push_back(u);
    }
    dfs1(1);
}

// main
int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    // cin >> T;
    for(int t = 1; t <= T; ++t) {
        // cout << "Case #" << t << ":\n";
        solve();
    }
    return 0;
}

