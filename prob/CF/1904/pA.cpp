// constants
const int dx[] = {1, 1, -1, -1};
const int dy[] = {1, -1, 1, -1};


// solution
void solve() {
    int a, b;
    cin >> a >> b;
    int xk, yk;
    cin >> xk >> yk;
    int xq, yq;
    cin >> xq >> yq;
    int ans = 0;
    set<pair<int, int> > p1, p2;
    for(int t = 0; t < 2; ++t) {
        for(int i = 0; i < 4; ++i) {
            p1.insert(make_pair(xk + dx[i] * a, yk + dy[i] * b));
            p2.insert(make_pair(xq + dx[i] * a, yq + dy[i] * b));
        }
        swap(a, b);
    }
    for(auto p : p1) {
        if(p2.count(p)) ++ans;
    }
    cout << ans << '\n';
}

// main
int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    cin >> T;
    for(int t = 1; t <= T; ++t) {
        // cout << "Case #" << t << ":\n";
        solve();
    }
    return 0;
}

