// solution
void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    // a[l] is larger
    ll cnt = 0;
    for (int l = 0; l < n; ++l) {
        for (int d = 1; d <= a[l] && 1ll * a[l] * d + l < n; ++d) {
            int r = a[l] * d + l;
            if (a[r] == d)
                ++cnt;
        }
    }
    // a[r] is larger
    for (int r = 0; r < n; ++r) {
        for (int d = 1; d < a[r] && r - 1ll * a[r] * d >= 0; ++d) {
            int l = r - a[r] * d;
            if (a[l] == d)
                ++cnt;
        }
    }
    cout << cnt << '\n';
}

// main
int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    cin >> T;
    for (int t = 1; t <= T; ++t) {
        // cout << "Case #" << t << ":\n";
        solve();
    }
    return 0;
}
