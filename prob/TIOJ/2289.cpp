using namespace std;
using ll = long long int;
FILE* setIO(string file = "") {
	ios::sync_with_stdio(0);
	cin.tie(0);
	if(file.empty()) return NULL;
	if(freopen((file + ".in").c_str(), "r", stdin) == NULL) return NULL; 
	return freopen((file + ".out").c_str(), "w", stdout);
}
const int MAXQ = 5e5 + 5;
pair<int, int> p[MAXQ];
void solve() {
	int n, q;
	cin >> n >> q;
	for(int i = 0; i < q; ++i) {
		cin >> p[i].first >> p[i].second;
	}
	sort(p, p + q);
	int last = -1, cnt = 0;
	for(int i = 0; i < q; ++i) {
		auto [l, r] = p[i];
		debug(l), debug(r);
		if(l > last) {
			last = r;
			++cnt;
		} else last = min(last, r);
	}
	cout << cnt << '\n';
}

int main() {
	setIO();
	int T = 1;
	//cin >> T;
	for(int t = 1; t <= T; ++t) {
		//cout << "Case " << t << ": ";
		solve();
	}
}

